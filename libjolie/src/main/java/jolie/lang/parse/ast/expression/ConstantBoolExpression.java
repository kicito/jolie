package jolie.lang.parse.ast.expression;

import jolie.lang.NativeType;
import jolie.lang.parse.OLVisitor;
import jolie.lang.parse.ast.OLSyntaxNode;
import jolie.lang.parse.context.ParsingContext;


public class ConstantBoolExpression extends OLSyntaxNode implements NativeLiteral
{
	private final boolean value;

	public ConstantBoolExpression( ParsingContext context, boolean value )
	{
		super( context );
		this.value = value;
	}
	
	public boolean value()
	{
		return value;
	}

	@Override
	public NativeType type(){
		return NativeType.BOOL;
	}
	
	@Override
	public void accept( OLVisitor visitor )
	{
		visitor.visit( this );
	}

	@Override
	public String toString()
	{
		return Boolean.toString(this.value);
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#hashCode()
	 */

	@Override
	public int hashCode()
	{
		final int prime = 31;
		int result = 1;
		result = prime * result + (value ? 1231 : 1237);
		return result;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#equals(java.lang.Object)
	 */

	@Override
	public boolean equals( Object obj )
	{
		if ( this == obj ) return true;
		if ( obj == null ) return false;
		if ( getClass() != obj.getClass() ) return false;
		ConstantBoolExpression other = (ConstantBoolExpression) obj;
		if ( value != other.value ) return false;
		return true;
	}
}
